import mongoose from "mongoose";

const donationSchema = new mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
  },
  customerId: {
    type: String,
  },
  amount: {
    type: Number,
    required: true,
  },
  currency: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    enum: ["succeeded", "failed", "pending"], // 'succeeded' or 'failed'
    required: true,
  },
  paymentIntentId: {
    type: String,
    required: true,
    unique: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Donation = new mongoose.model("Donation", donationSchema);
export default Donation;
